def addition(num1, num2)
  sum = num1 + num2
  puts sum
end

def subtraction(num1, num2)
  difference = num2 - num1
  puts difference
end

def division(num1, num2)
  quotient= num2 / num1
  puts quotient
end

def multiplication(num1, num2)
  product= num1 * num2
  puts product
end

def modulo(num1, num2)
  modulo = num2 % num1
  puts modulo
end

def square_root(num)
  root = Math.sqrt(num)
  puts root
end
